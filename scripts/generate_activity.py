class Salesforce:
    """Thin async Salesforce REST wrapper with JWT-bearer auth + 401 refresh."""

    def __init__(self, client: httpx.AsyncClient, api_version: str) -> None:
        self._client = client
        self.api_version = api_version
        self.login_url = _require("SF_LOGIN_URL").rstrip("/")
        self.client_id = _require("SF_CLIENT_ID")
        self.username = _require("SF_USERNAME")
        key_path = Path(_require("SF_PRIVATE_KEY_FILE"))
        self._private_key = key_path.read_text()
        self.instance_url: str = ""
        self._token: str = ""
        self.login_count = 0
        self.logout_count = 0

    @property
    def _base(self) -> str:
        return f"{self.instance_url}/services/data/v{self.api_version}"

    def _mint_jwt(self) -> str:
        payload = {
            "iss": self.client_id,
            "sub": self.username,
            "aud": self.login_url,
            "exp": datetime.now(UTC) + _JWT_LIFETIME,
        }
        return jwt.encode(payload, self._private_key, algorithm="RS256")

    async def login(self) -> None:
        """(Re-)authenticate. Each call produces a fresh Login event + stream."""
        resp = await self._client.post(
            f"{self.login_url}/services/oauth2/token",
            data={
                "grant_type": "urn:ietf:params:oauth:grant-type:jwt-bearer",
                "assertion": self._mint_jwt(),
            },
        )
        if not resp.is_success:
            raise SystemExit(f"auth failed HTTP {resp.status_code}: {resp.text}")
        body = resp.json()
        self._token = body["access_token"]
        self.instance_url = body["instance_url"].rstrip("/")
        self.login_count += 1
        log.info("authenticated (login #%d) -> %s", self.login_count, self.instance_url)

    async def revoke(self, token: str) -> None:
        """Revoke an access token — produces a Logout ELF event for that session."""
        if not token:
            return
        with contextlib.suppress(httpx.HTTPError):
            resp = await self._client.post(
                f"{self.login_url}/services/oauth2/revoke", data={"token": token}
            )
            if resp.is_success:
                self.logout_count += 1

    def _headers(self) -> dict[str, str]:
        return {"Authorization": f"Bearer {self._token}", "Content-Type": "application/json"}

    async def _request(self, method: str, url: str, **kw) -> httpx.Response:
        headers = {**self._headers(), **kw.pop("headers", {})}
        resp = await self._client.request(method, url, headers=headers, **kw)
        if resp.status_code == 401:  # token expired mid-run — re-auth once and retry
            await self.login()
            headers = {**self._headers(), **kw.pop("headers", {})}
            resp = await self._client.request(method, url, headers=headers, **kw)
        return resp

    async def create(self, sobject: str, fields: dict, quiet: bool = False) -> str | None:
        # allowSave=true overrides Salesforce duplicate-alert rules so the
        # finite name pool never hard-fails a create as a "duplicate".
        resp = await self._request(
            "POST", f"{self._base}/sobjects/{sobject}", json=fields,
            headers={"Sforce-Duplicate-Rule-Header": "allowSave=true"},
        )
        if resp.status_code == 201:
            return resp.json()["id"]
        # quiet=True for capability-probed objects: a missing feature is expected,
        # not an error worth a warning.
        emit = log.debug if quiet else log.warning
        emit("create %s failed HTTP %d: %s", sobject, resp.status_code, resp.text[:300])
        return None

    async def update(self, sobject: str, record_id: str, fields: dict) -> bool:
        resp = await self._request(
            "PATCH", f"{self._base}/sobjects/{sobject}/{record_id}", json=fields
        )
        if resp.status_code == 204:
            return True
        log.warning("update %s/%s failed HTTP %d", sobject, record_id, resp.status_code)
        return False

    async def query(self, soql: str) -> dict:
        resp = await self._request(
            "GET", f"{self._base}/query", params={"q": soql}
        )
        resp.raise_for_status()
        return resp.json()

    async def search(self, sosl: str) -> None:
        resp = await self._request("GET", f"{self._base}/search", params={"q": sosl})
        if not resp.is_success:
            log.debug("search failed HTTP %d: %s", resp.status_code, resp.text[:200])

    async def list_reports(self) -> list[str]:
        resp = await self._request("GET", f"{self._base}/analytics/reports")
        if not resp.is_success:
            return []
        return [r["id"] for r in resp.json()]

    async def run_report(self, report_id: str) -> None:
        resp = await self._request(
            "GET", f"{self._base}/analytics/reports/{report_id}",
            params={"includeDetails": "false"},
        )
        if not resp.is_success:
            log.debug("report %s failed HTTP %d", report_id, resp.status_code)

    async def run_apex(self, body: str) -> None:
        resp = await self._request(
            "GET", f"{self._base}/tooling/executeAnonymous/",
            params={"anonymousBody": body},
        )
        if not resp.is_success:
            log.debug("apex failed HTTP %d: %s", resp.status_code, resp.text[:200])

    async def bulk_insert(self, sobject: str, csv_body: str) -> None:
        create = await self._request(
            "POST", f"{self._base}/jobs/ingest",
            json={
                "object": sobject,
                "operation": "insert",
                "lineEnding": "LF",
                "contentType": "CSV",
            },
        )
        if create.status_code != 200:
            log.debug("bulk create job failed HTTP %d: %s", create.status_code, create.text[:200])
            return
        job_id = create.json()["id"]
        put = await self._request(
            "PUT", f"{self._base}/jobs/ingest/{job_id}/batches",
            content=csv_body, headers={"Content-Type": "text/csv"},
        )
        if put.status_code not in (200, 201):
            log.debug("bulk upload failed HTTP %d", put.status_code)
            return
        await self._request(
            "PATCH", f"{self._base}/jobs/ingest/{job_id}",
            json={"state": "UploadComplete"},
        )

    async def bulk_query(self, soql: str) -> None:
        """Submit a Bulk API 2.0 query job (BulkApi ELF events, query flavour)."""
        resp = await self._request(
            "POST", f"{self._base}/jobs/query", json={"operation": "query", "query": soql}
        )
        if resp.status_code not in (200, 201):
            log.debug("bulk query failed HTTP %d: %s", resp.status_code, resp.text[:200])

    async def describe_global(self) -> None:
        resp = await self._request("GET", f"{self._base}/sobjects/")
        if not resp.is_success:
            log.debug("describe global failed HTTP %d", resp.status_code)

    async def describe_sobject(self, sobject: str) -> None:
        resp = await self._request("GET", f"{self._base}/sobjects/{sobject}/describe/")
        if not resp.is_success:
            log.debug("describe %s failed HTTP %d", sobject, resp.status_code)

    async def composite_batch(self, subrequests: list[dict]) -> None:
        """Run a batch of subrequests in one round-trip (composite API usage)."""
        resp = await self._request(
            "POST", f"{self._base}/composite/batch", json={"batchRequests": subrequests}
        )
        if not resp.is_success:
            log.debug("composite batch failed HTTP %d: %s", resp.status_code, resp.text[:200])

    async def upload_file(
        self, title: str, filename: str, body: bytes, description: str
    ) -> str | None:
        """Upload a ContentVersion (file). Produces ContentTransfer/file ELF events."""
        return await self.create(
            "ContentVersion",
            {
                "Title": title,
                "PathOnClient": filename,
                "VersionData": base64.b64encode(body).decode("ascii"),
                "Description": description,
            },
        )

    async def download_file(self, content_version_id: str) -> None:
        """Download a file's binary — produces a file-transfer ELF event."""
        resp = await self._request(
            "GET", f"{self._base}/sobjects/ContentVersion/{content_version_id}/VersionData"
        )
        if not resp.is_success:
            log.debug("file download failed HTTP %d", resp.status_code)

    async def run_report_async(self, report_id: str) -> None:
        resp = await self._request(
            "POST", f"{self._base}/analytics/reports/{report_id}/instances"
        )
        if not resp.is_success:
            log.debug("async report %s failed HTTP %d", report_id, resp.status_code)

    async def list_dashboards(self) -> list[str]:
        resp = await self._request("GET", f"{self._base}/analytics/dashboards")
        if not resp.is_success:
            return []
        payload = resp.json()
        items = payload.get("dashboards", payload) if isinstance(payload, dict) else payload
        return [d["id"] for d in items if isinstance(d, dict) and "id" in d]

    async def get_dashboard(self, dashboard_id: str) -> None:
        resp = await self._request("GET", f"{self._base}/analytics/dashboards/{dashboard_id}")
        if not resp.is_success:
            log.debug("dashboard %s failed HTTP %d", dashboard_id, resp.status_code)

    async def delete_many(self, ids: list[str]) -> int:
        """Delete up to 200 records via the sObject Collections API. Returns count deleted."""
        deleted = 0
        for i in range(0, len(ids), 200):
            chunk = ids[i : i + 200]
            resp = await self._request(
                "DELETE", f"{self._base}/composite/sobjects",
                params={"ids": ",".join(chunk), "allOrNone": "false"},
            )
            if resp.is_success:
                deleted += sum(1 for r in resp.json() if r.get("success"))
            else:
                log.warning("delete batch failed HTTP %d: %s", resp.status_code, resp.text[:200])
        return deleted


