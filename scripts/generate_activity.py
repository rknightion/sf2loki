def _read_csv_rows(path: Path, ncols: int) -> list[list[str]]:
    """Parse simple comma-separated rows, skipping blanks, comments, malformed."""
    rows: list[list[str]] = []
    if not path.exists():
        return rows
    for raw in path.read_text().splitlines():
        line = raw.strip()
        if not line or line.startswith("#"):
            continue
        parts = [p.strip() for p in line.split(",")]
        if len(parts) != ncols or any(not p for p in parts):
            continue
        rows.append(parts)
    return rows


